                            Incidentally, the first known use of the word “gymkhana” is said to have been around 1877.
                            It was probably a modification of the Hindi ‘gedkhana’ and Urdu ‘gendkhana’ (literally, ball
                            court), In a lighter vein, people say that after a session at the gym you are ready for
                            great ‘khana'(food).<br>
                            Wodehouse Gymkhana was started in 1909 as Bombay Commercial Gymkhana. In 1935, the name was
                            changed to Wodehouse Gymkhana, after Sir Philips Wodehouse, who was Governor of Bombay from
                            1872-75. There is said to be a connection between this Wodehouse and his more famous
                            namesake PG Wodehouse, who has a great fan following in India.<br>
                            Wodehouse Gymkhana stands on Wodehouse Road (now re-named Nathalal Parekh Marg after Subhas
                            Chandra Bose’s treasurer).<br>
                            Like other gymkhanas in the city, Wodehouse Gymkhana was strictly a white man’s club.
                            Indians were not allowed in, even as guests. It as began to change after Independence when a
                            few Indian names started appearing among the guests. The first Indian President was the
                            hugely popular Sam Aga, still remembered for his camaraderie. He would look out of the
                            window of the billiards room at the long verandah, still unchanged from those days, and
                            challenged members to a game. The loser has to pay a penalty of 50 paise.<br>
                            Wodehouse Gymkhana is not without its share of gossip and scandals. The names of lady guests
                            were never entered in the register. The reason: discretion. They happened to be mistresses
                            whom members could entertain without fear of being found out.</p>
